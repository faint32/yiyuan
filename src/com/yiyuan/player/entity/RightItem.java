package com.yiyuan.player.entity;

/**
 * 右侧菜单对应的item类
 * @author lenovo
 *
 */
public class RightItem {

	private String name;
	private String tag;
	private String nav_icon;
	private String home_icon;
	private String type;
	private String base_url;
	private String filter;
	private String support;
	private String extra;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getNav_icon() {
		return nav_icon;
	}
	public void setNav_icon(String nav_icon) {
		this.nav_icon = nav_icon;
	}
	public String getHome_icon() {
		return home_icon;
	}
	public void setHome_icon(String home_icon) {
		this.home_icon = home_icon;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getBase_url() {
		return base_url;
	}
	public void setBase_url(String base_url) {
		this.base_url = base_url;
	}
	public String getFilter() {
		return filter;
	}
	public void setFilter(String filter) {
		this.filter = filter;
	}
	public String getSupport() {
		return support;
	}
	public void setSupport(String support) {
		this.support = support;
	}
	public String getExtra() {
		return extra;
	}
	public void setExtra(String extra) {
		this.extra = extra;
	}
	
	
}
